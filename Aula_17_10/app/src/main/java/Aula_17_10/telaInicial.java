    private void btnSomarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSomarActionPerformed
        //System.out.println("botao clicado");
        // Para pegar o texto de algum campo, usamos o metodo nomeDoComponente.getText();
        String numero1 = numero.getText();
        String numero2 = outroNumero.getText();
        
        // O método Integer.parseInt() converte um numero em String para int
        int numero1Int = Integer.parseInt(numero1);
        int numero2Int = Integer.parseInt(numero2);
        
        int soma = numero1Int + numero2Int;
        // Para trocar o texto de algum componente, usamos o método nomeDoComponente.setText("novo texto")
        results.setText("Resultado: " + soma);
    }//GEN-LAST:event_btnSomarActionPerformed

    private void numeroFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_numeroFocusGained
        // Se o texto do campo for igual a 'Digite um numero...'
        // ele apaga o texto
        // para isso, primeiro, eu preciso pegar o texto escrito no campo
        // e ai, comparar se é igual a 'Digite um numero'
        if ("Digite um numero...".equals(numero.getText())){
        numero.setText("");
        }
    }//GEN-LAST:event_numeroFocusGained

    private void numeroFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_numeroFocusLost
        if ("".equals(numero.getText())){
            numero.setText("Digite um numero...");
        }
    }//GEN-LAST:event_numeroFocusLost

